function accum(s) {
    let result = '';
    for (let i = 0; i < s.length; i++) {
        let letter = s[i]
        letter = letter.repeat(i + 1)
        result += letter.charAt(0).toUpperCase() + letter.substring(1) + '-'

    }
    return result.substr(0, result.length - 1);

}

